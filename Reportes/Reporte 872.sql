	,CASE(ser.SERAPLPRO) WHEN 0 THEN 'NO' WHEN 1 THEN 'SI' END AS aplica
	,CONVERT(BIGINT, ser.SERCANTID) Cantidad, CONVERT(BIGINT, ser.SERVALENT) Val_ent, CONVERT(BIGINT, ser.SERVALPAC) Val_pac
