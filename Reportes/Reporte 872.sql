	,cxcc.CCVALOR tot_fac, cxcc.CCVALDEB debitos, cxcc.CCVALCRE creditos
	,cxcc.CCSALDO saldo, det.GDECODIGO Cod_Con, det.GDENOMBRE Plan_Beneficios
	,COALESCE (aresp.gascodigo, ares.gascodigo) Area_Solicita, arep.GASCODIGO Area_Presta
	,COALESCE (ips.SIPCODIGO, prod.IPRCODIGO) codigo_principal, COALESCE (ips.SIPNOMBRE, prod.IPRDESCOR) nombre
	,ips1.SIPCODIGO AS sub_codigo, CONVERT(BIGINT, paq.SPHVALSER) val_sub
